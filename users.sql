CREATE TABLE users (
    userid: ,
     first_name   varchar(100) NOT NULL,
     last_name   varchar(100) NOT NULL,
     email  varchar(50) NOT NULL,
     address   varchar(500) NOT NULL,
     status varchar(20),
     password  varchar(10),
     otp    varchar(10),
     dob    Date,
     userType  varchar(20)
);
